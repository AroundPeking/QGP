
	SUBROUTINE DNEWT(X,EPS,FS,L)
	L=60
	CALL FS(X,F,DY)
10	IF (ABS(DY)+1.0.EQ.1.0) THEN
	  L=0
	  WRITE(*,20)
	  RETURN
	END IF
20	FORMAT(1X,'  ERR')
	X1=X-F/DY
	CALL FS(X1,F,DY)
	IF ((ABS(X1-X).GE.EPS).OR.(ABS(F).GE.EPS)) THEN
	  L=L-1
	  X=X1
	  IF (L.EQ.0) RETURN
	  GOTO 10
	END IF
	X=X1
	RETURN
	END

