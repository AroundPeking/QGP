
	SUBROUTINE FLAGS(F,G)
	DIMENSION T(5),C(5)
	DOUBLE PRECISION F,G,T,C,X
	DATA C/0.6790941054,1.638487956,2.769426772,
     *         4.31594400,7.104896230/
	DATA T/0.26355990,1.41340290,3.59642600,
     *         7.08580990,12.64080000/

	G=0.0D0
	DO 10 I=1,5
	  X=T(I)
	  G=G+F(X)*C(I)
10	CONTINUE
	END


