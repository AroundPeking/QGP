
	SUBROUTINE FPART(AA,BB,N,M,A,B,S1,S2)
	DIMENSION A(N),B(N),SA(4),SB(4),CA(4),CB(4)
	DOUBLE PRECISION AA,BB,A,B,S1,S2,SA,SB,CA,CB
	SMA=SIN(M*AA)
	SMB=SIN(M*BB)
	CMA=COS(M*AA)
	CMB=COS(M*BB)
	SA(1)=SMA
	SA(2)=CMA
	SA(3)=-SMA
	SA(4)=-CMA
	SB(1)=SMB
	SB(2)=CMB
	SB(3)=-SMB
	SB(4)=-CMB
	CA(1)=CMA
	CA(2)=-SMA
	CA(3)=-CMA
	CA(4)=SMA
	CB(1)=CMB
	CB(2)=-SMB
	CB(3)=-CMB
	CB(4)=SMB
	S1=0.0
	S2=0.0
	MM=1
	DO 10 K=1,N
	  L=K
20	  IF (L.GE.5) THEN
	    L=L-4
	    GOTO 20
	  END IF
	  MM=MM*M
	  S1=S1+(B(K)*SB(L)-A(K)*SA(L))/(1.0*MM)
	  S2=S2+(B(K)*CB(L)-A(K)*CA(L))/(1.0*MM)
10	CONTINUE
	S2=-S2
	RETURN
	END

