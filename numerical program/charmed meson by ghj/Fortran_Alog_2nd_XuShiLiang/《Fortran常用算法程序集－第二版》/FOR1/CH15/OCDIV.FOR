
	SUBROUTINE OCDIV(PR,PI,M,QR,QI,N,SR,SI,K,RR,RI,L)
	DOUBLE PRECISION PR(M),PI(M),QR(N),QI(N)
	DOUBLE PRECISION SR(K),SI(K),RR(L),RI(L)
	DOUBLE PRECISION U,V,D
	DO 10 I=1,K
	  SR(I)=0.0
	  SI(I)=0.0
10	CONTINUE
	D=QR(N)*QR(N)+QI(N)*QI(N)
	IF (D+1.0.EQ.1.0) RETURN
	LL=M-1
	DO 50 I=K,1,-1
	  CALL PCDIV(PR(LL+1),PI(LL+1),QR(N),QI(N),U,V)
	  SR(I)=U
	  SI(I)=V
	  MM=LL
	  DO 30 J=1,N-1
	    CALL PCMUL(SR(I),SI(I),QR(N-J),QI(N-J),U,V)
	    PR(MM)=PR(MM)-U
	    PI(MM)=PI(MM)-V
	    MM=MM-1
30	  CONTINUE
	  LL=LL-1
50	CONTINUE
	DO 80 I=1,L
	  RR(I)=PR(I)
	  RI(I)=PI(I)
80	CONTINUE
	RETURN
	END

