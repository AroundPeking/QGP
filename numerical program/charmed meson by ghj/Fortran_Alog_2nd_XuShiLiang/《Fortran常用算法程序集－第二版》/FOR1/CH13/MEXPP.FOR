

	FUNCTION MEXPP(X)
	DOUBLE PRECISION MEXPP,X,FLRGS,Y,R
	EXTERNAL EI
	DOUBLE PRECISION EI
	IF (X.LE.0.0) THEN
	  MEXPP=-1.0D+35
	  RETURN
	END IF
	Y=X
	R=0.57721566490153286060651
	MEXPP=FLRGS(0.0D0,X,EI,1.0E-07)
	MEXPP=R+LOG(Y)+MEXPP
	RETURN
	END

	FUNCTION EI(X)
	DOUBLE PRECISION EI,X
	EI=(EXP(-X)-1.0)/X
	RETURN
	END



