

	DIMENSION X(250),Y(250),Z(250)
	REAL NGRN1
	DOUBLE PRECISION R
	N=250
	WRITE(*,*)
	WRITE(*,5)
5	FORMAT(1X,'A,B,C=?')
	READ(*,*) A,B,C
	WRITE(*,100) A,B,C
100	FORMAT(1X,3F10.6)
	DT=0.04
	R=1.0
	DO 20 I=1,N
	  S=NGRN1(0.0,0.5,R)
	  T=I*DT
	  Z(I)=3.0*T*T-2.0*T+5.0
	  X(I)=Z(I)+S
20	CONTINUE
	WRITE(*,*)
	CALL KKABG(N,X,DT,A,B,C,Y)
	WRITE(*,30)
30	FORMAT(4X,'T',16X,'X(T)',11X,'Y(T)',11X,'Z(T)')
	DO 40 I=1,N,5
	  T=I*DT
	  WRITE(*,50) T,X(I),Y(I),Z(I)
40	CONTINUE
50	FORMAT(1X,F5.2,10X,3E15.6)
	WRITE(*,*)
	END


