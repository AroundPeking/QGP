
	EXTERNAL F,FJ
	DIMENSION X(2),P(2,2),D(2),PP(2,2),DX(2),U(2,2),V(2,2)
	DIMENSION S(3),E(3),WORK(3)
	DOUBLE PRECISION X,P,D,PP,DX,U,V,S,E,WORK
	DATA X/0.5,-1.0/
	M=2
	N=2
	KA=3
	EPS1=0.000001
	EPS2=0.000001
	CALL DNGIN(M,N,X,EPS1,EPS2,F,FJ,L,P,D,PP,DX,U,V,KA,S,E,WORK)
	WRITE(*,*)
	WRITE(*,5) L
5	FORMAT(1X,'L=',I4)
	WRITE(*,*)
	IF (L.EQ.0) THEN
	  WRITE(*,10) X(1),X(2)
	  WRITE(*,*)
	END IF
10	FORMAT(1X,'X(1)=',D13.6,3X,'X(2)=',D13.6)
	END

	SUBROUTINE F(M,N,X,D)
	DIMENSION X(N),D(M)
	DOUBLE PRECISION X,D
	D(1)=X(1)*X(1)+10.0*X(1)*X(2)+4.0*X(2)*X(2)+0.7401006
	D(2)=X(1)*X(1)-3.0*X(1)*X(2)+2.0*X(2)*X(2)-1.0201228
	RETURN
	END

	SUBROUTINE FJ(M,N,X,P)
	DIMENSION X(N),P(M,N)
	DOUBLE PRECISION X,P
	P(1,1)=2.0*X(1)+10.0*X(2)
	P(1,2)=10.0*X(1)+8.0*X(2)
	P(2,1)=2.0*X(1)-3.0*X(2)
	P(2,2)=-3.0*X(1)+4.0*X(2)
	RETURN
	END

