
	DIMENSION X(11),Y(11),Z(11,11)
	DOUBLE PRECISION X,Y,Z,U,V,W
	DO 10 I=1,11
	X(I)=0.1*(I-1)
10	Y(I)=X(I)
	DO 30 I=1,11
	DO 30 J=1,11
30	Z(I,J)=EXP(-(X(I)-Y(J)))
	U=0.35
	V=0.65
	CALL ESLGQ(X,Y,Z,11,11,U,V,W)
	WRITE(*,40) U,V,W
40	FORMAT(1X,'X=',F7.3,5X,'Y=',F7.3,10X,'Z(X,Y)=',D15.6)
	U=0.45
	V=0.55
	CALL ESLGQ(X,Y,Z,11,11,U,V,W)
	WRITE(*,40) U,V,W
	END

