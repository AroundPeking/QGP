

	FUNCTION MCOSS(X)
	DOUBLE PRECISION MCOSS,X,FLRGS,Y,R
	EXTERNAL CI
	DOUBLE PRECISION CI
	IF (X.LE.0.0) THEN
	  MCOSS=-1.0D+35
	  RETURN
	END IF
	Y=X
	R=0.57721566490153286060651
	MCOSS=FLRGS(0.0D0,X,CI,1.0E-07)
	MCOSS=R+LOG(Y)-MCOSS
	RETURN
	END

	FUNCTION CI(X)
	DOUBLE PRECISION CI,X
	CI=(1.0-COS(X))/X
	RETURN
	END



