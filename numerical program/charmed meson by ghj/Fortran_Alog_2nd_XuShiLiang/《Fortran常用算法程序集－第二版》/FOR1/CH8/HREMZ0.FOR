
	EXTERNAL F1,F2
	DIMENSION P(5),Q(7)
	DOUBLE PRECISION P,Q,A,B
	A=-1.0
	B=1.0
	EPS=1.0E-10
	WRITE(*,*)
	CALL HREMZ(A,B,F1,P,4,5,EPS)
	WRITE(*,10) (I,P(I),I=1,4)
10	FORMAT(1X,'P(',I2,' )=',D15.6)
	WRITE(*,30) P(5)
	WRITE(*,*)
	CALL HREMZ(A,B,F2,Q,6,7,EPS)
	WRITE(*,20) (I,Q(I),I=1,6)
20	FORMAT(1X,'Q(',I2,' )=',D15.6)
	WRITE(*,30) Q(7)
30	FORMAT(1X,'HMAX=',D15.6)
	END

	FUNCTION F1(X)
	DOUBLE PRECISION X
	F1=EXP(X)
	RETURN
	END

	FUNCTION F2(X)
	DOUBLE PRECISION X
	F2=ATAN(X)
	RETURN
	END

