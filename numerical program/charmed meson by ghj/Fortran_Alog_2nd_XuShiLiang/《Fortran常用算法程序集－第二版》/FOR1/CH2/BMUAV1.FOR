
	DIMENSION A(3,4),U(3,3),V(4,4),C(3,4),S(5),E(5),WORK(5)
	DOUBLE PRECISION A,U,V,C,S,E,WORK
	DATA A/1.0,2.0,3*1.0,2*-1.0,2*0.0,-1.0,2.0,1.0/
	M=3
	N=4
	KA=5
	EPS=0.000001
	CALL BMUAV(A,M,N,U,V,L,EPS,KA,S,E,WORK)
	WRITE(*,*)
	WRITE(*,10) L
10	FORMAT(1X,'L=',I3)
	WRITE(*,*)
	WRITE(*,20)
20	FORMAT(1X,'MAT U:')
	WRITE(*,30) ((U(I,J),J=1,M),I=1,M)
30	FORMAT(1X,3D15.6)
	WRITE(*,*)
	WRITE(*,40)
40	FORMAT(1X,'MAT A:')
	WRITE(*,50) ((A(I,J),J=1,N),I=1,M)
50	FORMAT(1X,4D15.6)
	WRITE(*,*)
	WRITE(*,60)
60	FORMAT(1X,'MAT V:')
	WRITE(*,70) ((V(I,J),J=1,N),I=1,N)
70	FORMAT(1X,4D15.6)
	WRITE(*,*)
	CALL BRMUL(U,A,3,3,4,C)
	CALL BRMUL(C,V,3,4,4,A)
	WRITE(*,80)
80	FORMAT(1X,'MAT UAV:')
	WRITE(*,50) ((A(I,J),J=1,N),I=1,M)
	WRITE(*,*)
	END

