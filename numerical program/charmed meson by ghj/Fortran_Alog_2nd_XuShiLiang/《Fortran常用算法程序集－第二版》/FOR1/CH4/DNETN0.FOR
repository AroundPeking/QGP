
	EXTERNAL FS
	DIMENSION X(3),Y(3),A(3,3),B(3),JS(3)
	DOUBLE PRECISION X,Y,A,B,T,H
	DATA X/1.0,1.0,1.0/
	EPS=1.0E-07
	T=0.1
	H=0.1
	CALL DNETN(3,X,Y,EPS,FS,T,H,A,B,L,JS)
	WRITE(*,*)
	WRITE(*,30) L
	WRITE(*,*)
	DO 10 I=1,3
10	WRITE(*,20) I,X(I)
20	FORMAT(1X,'X(',I2,' )=',D15.6)
30	FORMAT(1X,'L=',I4)
	WRITE(*,*)
	END

	SUBROUTINE FS(X,Y,N)
	DIMENSION X(N),Y(N)
	DOUBLE PRECISION X,Y
	Y(1)=X(1)*X(1)+X(2)*X(2)+X(3)*X(3)-1.0
	Y(2)=2.0*X(1)*X(1)+X(2)*X(2)-4.0*X(3)
	Y(3)=3.0*X(1)*X(1)-4.0*X(2)+X(3)*X(3)
	RETURN
	END

