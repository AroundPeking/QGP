
	EXTERNAL F
	DIMENSION Y(3),D(3),Z(3,11),Q(3),U(3),V(3),G(3)
	DOUBLE PRECISION Y,D,Z,A,H,T,Q,U,V,G
	DATA Y/0.0,1.0,1.0/
	A=0.0
	H=0.1
	N=11
	M=3
	EPS=0.000001
	CALL GGIL2(A,H,N,Y,M,Z,F,EPS,D,Q,U,V,G)
	WRITE(*,*)
	DO 10 I=1,N
	  T=(I-1)*H
	  WRITE(*,20) T
	  WRITE(*,30) (Z(J,I),J=1,M)
	  WRITE(*,*)
10	CONTINUE
20	FORMAT(1X,'T=',F7.3)
30	FORMAT(1X,'Y1=',D13.6,5X,'Y2=',D13.6,5X,'Y3=',D13.6)
	END

	SUBROUTINE F(T,Y,M,D)
	DIMENSION Y(M),D(M)
	DOUBLE PRECISION Y,D,T
	D(1)=Y(2)
	D(2)=-Y(1)
	D(3)=-Y(3)
	RETURN
	END

