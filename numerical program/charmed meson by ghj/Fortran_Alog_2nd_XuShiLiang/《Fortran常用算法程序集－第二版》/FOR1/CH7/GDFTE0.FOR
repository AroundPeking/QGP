
	EXTERNAL FS
	DIMENSION Y(11),G(66),D(22)
	DOUBLE PRECISION Y,A,B,Y0,YN,G,D
	A=2.0
	B=3.0
	N=11
	N2=22
	N6=66
	Y0=0.0
	YN=0.0
	CALL GDFTE(A,B,Y0,YN,N,Y,FS,N2,N6,G,D)
	WRITE(*,*)
	WRITE(*,10) (I,Y(I),I=1,N)
10	FORMAT(1X,'Y(',I2,' )=',D15.6)
	WRITE(*,*)
	END

	SUBROUTINE FS(X,U,V,W,F)
	DOUBLE PRECISION X,U,V,W,F
	U=-1.0
	V=0.0
	W=2.0/(X*X)
	F=1.0/X
	RETURN
	END


