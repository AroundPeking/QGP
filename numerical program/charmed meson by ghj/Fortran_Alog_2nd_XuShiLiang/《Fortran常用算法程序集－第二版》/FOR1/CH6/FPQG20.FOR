
	EXTERNAL FS,F
	DOUBLE PRECISION F,A,B,S
	A=0.0
	B=1.0
	EPS=0.00005
	CALL FPQG2(A,B,FS,F,EPS,S)
	WRITE(*,10) S
10	FORMAT(1X,'S=',D13.6)
	END

	SUBROUTINE FS(X,Y1,Y2)
	DOUBLE PRECISION X,Y1,Y2,Q
	Q=SQRT(1.0-X*X)
	Y1=-Q
	Y2=Q
	RETURN
	END

	FUNCTION F(X,Y)
	DOUBLE PRECISION F,X,Y
	F=EXP(X*X+Y*Y)
	RETURN
	END

