

	EXTERNAL FA
	DOUBLE PRECISION X,Z
	WRITE(*,*)
	WRITE(*,5)
	READ(*,*) EPS
	X=1.0
	CALL JMAX1(X,Z,K,M,EPS,FA)
	WRITE(*,*)
	WRITE(*,40) M
	WRITE(*,*)
	IF (K.EQ.0) THEN
	  WRITE(*,10)
	ELSE
	  WRITE(*,20)
	END IF
	WRITE(*,30) X,Z
	WRITE(*,*)
5	FORMAT(1X,'EPS=')
10	FORMAT(1X,'MIN:')
20	FORMAT(1X,'MAX:')
30	FORMAT(1X,'X=',D15.6,5X,'F(X)=',D15.6)
40	FORMAT(1X,'M=',I2)
	END

	SUBROUTINE FA(X,Y,DY)
	DOUBLE PRECISION X,Y,DY
	Y=(X-1.0)*(10.0-X)
	DY=-2.0*X+11.0
	RETURN
	END

