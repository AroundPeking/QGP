
	SUBROUTINE ILOG1(X,Y,N,T,A,B,Q,S,UMAX,UMIN,U)
	DIMENSION X(N),Y(N)
	DOUBLE PRECISION X,Y,T,A,B,Q,S,UMAX,UMIN,U
	DOUBLE PRECISION XX,YY,DX,DXY
	XX=0.0
	YY=0.0
	DO 10 I=1,N
	  XX=XX+X(I)/N
	  YY=YY+(LOG(Y(I))/LOG(T))/N
10	CONTINUE
	DX=0.0
	DXY=0.0
	DO 20 I=1,N
	  Q=X(I)-XX
	  DX=DX+Q*Q
	  DXY=DXY+Q*(LOG(Y(I))/LOG(T)-YY)
20	CONTINUE
	A=DXY/DX
	B=YY-A*XX
	B=T**B
	Q=0.0
	U=0.0
	UMAX=0.0
	UMIN=1.0D+30
	DO 30 I=1,N
	  S=B*T**(A*X(I))
	  Q=Q+(Y(I)-S)*(Y(I)-S)
	  DX=ABS(Y(I)-S)
	  IF (DX.GT.UMAX) UMAX=DX
	  IF (DX.LT.UMIN) UMIN=DX
	  U=U+DX/N
30	CONTINUE
	S=SQRT(Q/N)
	RETURN
	END

