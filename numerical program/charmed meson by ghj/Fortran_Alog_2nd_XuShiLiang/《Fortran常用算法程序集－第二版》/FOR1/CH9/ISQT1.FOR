
	SUBROUTINE ISQT1(X,Y,N,A,B,Q,S,P,UMAX,UMIN,U)
	DIMENSION X(N),Y(N)
	DOUBLE PRECISION X,Y,A,B,Q,S,P,UMAX,UMIN,U
	DOUBLE PRECISION XX,YY,DX,DXY
	XX=0.0
	YY=0.0
	DO 10 I=1,N
	  XX=XX+X(I)/N
	  YY=YY+Y(I)/N
10	CONTINUE
	DX=0.0
	DXY=0.0
	DO 20 I=1,N
	  Q=X(I)-XX
	  DX=DX+Q*Q
	  DXY=DXY+Q*(Y(I)-YY)
20	CONTINUE
	A=DXY/DX
	B=YY-A*XX
	Q=0.0
	U=0.0
	P=0.0
	UMAX=0.0
	UMIN=1.0D+30
	DO 30 I=1,N
	  S=A*X(I)+B
	  Q=Q+(Y(I)-S)*(Y(I)-S)
	  P=P+(S-YY)*(S-YY)
	  DX=ABS(Y(I)-S)
	  IF (DX.GT.UMAX) UMAX=DX
	  IF (DX.LT.UMIN) UMIN=DX
	  U=U+DX/N
30	CONTINUE
	S=SQRT(Q/N)
	RETURN
	END

