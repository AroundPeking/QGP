
	EXTERNAL F,FJ
	DIMENSION X(2),P(3,2),D(3),PP(2,3),DX(2),U(3,3),V(2,2)
	DIMENSION S(4),E(4),WORK(4)
	DOUBLE PRECISION X,P,D,PP,DX,U,V,S,E,WORK
	DATA X/1.0,-1.0/
	M=3
	N=2
	KA=4
	EPS1=0.000001
	EPS2=0.000001
	CALL DNGIN(M,N,X,EPS1,EPS2,F,FJ,L,P,D,PP,DX,U,V,KA,S,E,WORK)
	WRITE(*,*)
	WRITE(*,5) L
5	FORMAT(1X,'L=',I4)
	WRITE(*,*)
	IF (L.EQ.0) THEN
	  WRITE(*,10) X(1),X(2)
	  WRITE(*,*)
	END IF
10	FORMAT(1X,'X(1)=',D13.6,3X,'X(2)=',D13.6)
	END

	SUBROUTINE F(M,N,X,D)
	DIMENSION X(N),D(M)
	DOUBLE PRECISION X,D
	D(1)=X(1)*X(1)+7.0*X(1)*X(2)+3.0*X(2)*X(2)+0.5
	D(2)=X(1)*X(1)-2.0*X(1)*X(2)+1.0*X(2)*X(2)-1.0
	D(3)=X(1)+X(2)+1.0
	RETURN
	END

	SUBROUTINE FJ(M,N,X,P)
	DIMENSION X(N),P(M,N)
	DOUBLE PRECISION X,P
	P(1,1)=2.0*X(1)+7.0*X(2)
	P(1,2)=7.0*X(1)+6.0*X(2)
	P(2,1)=2.0*X(1)-2.0*X(2)
	P(2,2)=-2.0*X(1)+2.0*X(2)
	P(3,1)=1.0
	P(3,2)=1.0
	RETURN
	END

