
	SUBROUTINE OCMUL(PR,PI,M,QR,QI,N,SR,SI,L)
	DOUBLE PRECISION PR(M),QR(N),SR(L),PI(M),QI(N),SI(L)
	DOUBLE PRECISION U,V
	DO 10 I=1,L
	  SR(I)=0.0
	  SI(I)=0.0
10	CONTINUE
	DO 50 I=1,M
	DO 50 J=1,N
	  K=I+J-1
	  CALL PCMUL(PR(I),PI(I),QR(J),QI(J),U,V)
	  SR(K)=SR(K)+U
	  SI(K)=SI(K)+V
50	CONTINUE
	RETURN
	END

