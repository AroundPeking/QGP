
	SUBROUTINE PPLOT(N,A,XC,YC,C,XD,YD)
	DIMENSION A(N,2)
	CHARACTER XY(72,72)
	CHARACTER XC,YC,C
	DO 10 I=1,72
	DO 10 J=1,72
10	XY(I,J)=' '
	XY(1,36)='Y'
	DO 20 I=2,72
20	XY(I,36)=YC
	XY(37,72)='X'
	DO 30 J=1,71
30	XY(37,J)=XC
	XD=0.0
	YD=0.0
	DO 40 K=1,N
	  IF (ABS(A(K,1)).GT.XD) XD=ABS(A(K,1))
	  IF (ABS(A(K,2)).GT.YD) YD=ABS(A(K,2))
40	CONTINUE
	XD=35/XD
	YD=35/YD
	DO 50 K=1,N
	  I=37.0-YD*A(K,2)+0.5
	  J=XD*A(K,1)+36.0+0.5
	  XY(I,J)=C
50	CONTINUE
	WRITE(*,*)
	DO 60 I=1,72
	  WRITE(*,70) (XY(I,J),J=1,72)
60	CONTINUE
70	FORMAT(1X,72A1)
	WRITE(*,*)
	WRITE(*,80) XD
80	FORMAT(1X,'X-SCALE IS: ',E15.6)
	WRITE(*,90) YD
90	FORMAT(1X,'Y-SCALE IS: ',E15.6)
	WRITE(*,*)
	RETURN
	END

