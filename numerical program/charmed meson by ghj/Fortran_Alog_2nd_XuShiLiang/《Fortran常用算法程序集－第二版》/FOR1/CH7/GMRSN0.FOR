
	EXTERNAL F
	DIMENSION Y(2),D(2),Z(2,11),U(2),AA(2),B(2),C(2),V(2)
	DOUBLE PRECISION Y,D,Z,A,T,H,U,AA,B,C,V
	A=0.0
	H=0.1
	N=11
	M=2
	EPS=0.00001
	Y(1)=0.0
	Y(2)=1.0
	CALL GMRSN(A,H,N,Y,M,Z,F,EPS,D,U,AA,B,C,V)
	WRITE(*,*)
	DO 10 I=1,N
	  T=(I-1)*H
	  WRITE(*,20) T,Z(1,I),Z(2,I)
	  WRITE(*,*)
10	CONTINUE
20	FORMAT(1X,F7.3,5X,'Y1=',D13.6,5X,'Y2=',D13.6)
	END

	SUBROUTINE F(T,Y,M,D)
	DIMENSION Y(M),D(M)
	DOUBLE PRECISION Y,D,T,Q
	Q=60*(0.06+T*(T-0.6))
	D(1)=Q*Y(2)
	D(2)=-Q*Y(1)
	RETURN
	END

