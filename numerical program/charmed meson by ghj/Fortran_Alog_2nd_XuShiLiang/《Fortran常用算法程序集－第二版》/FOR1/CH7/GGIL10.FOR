
	EXTERNAL F
	DIMENSION Y(3),D(3),Q(3),U(3),V(3),G(3)
	DOUBLE PRECISION Y,D,Q,T,H,U,V,G
	DATA Y/0.0,1.0,1.0/
	DATA Q/3*0.0/
	T=0.0
	H=0.1
	M=3
	EPS=0.000001
	WRITE(*,*)
	WRITE(*,20) T
	WRITE(*,30) (Y(J),J=1,M)
	WRITE(*,*)
	DO 10 I=1,10
	  CALL GGIL1(T,H,Y,M,F,EPS,Q,D,U,V,G)
	  T=I*H
	  WRITE(*,20) T
	  WRITE(*,30) (Y(J),J=1,M)
	  WRITE(*,*)
10	CONTINUE
20	FORMAT(1X,'T=',F7.3)
30	FORMAT(1X,'Y1=',D13.6,5X,'Y2=',D13.6,5X,'Y3=',D13.6)
	END

	SUBROUTINE F(T,Y,M,D)
	DIMENSION Y(M),D(M)
	DOUBLE PRECISION Y,D,T
	D(1)=Y(2)
	D(2)=-Y(1)
	D(3)=-Y(3)
	RETURN
	END

